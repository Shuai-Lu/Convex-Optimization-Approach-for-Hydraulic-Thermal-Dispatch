                    model_fixed.var.heatingnetwork.aux_M_alpha(i,:,t) <= ...
                    aux_alpha_max(i,:,t) .* model_fixed.var.heatingnetwork.Massflow(t+num_initialtime+1-i,:) + ...
                    model_fixed.var.heatingnetwork.aux_alpha(i,:,t) .* (ones(num_aux,1)*Massflow_min(t,:)) - ...
                    aux_alpha_max(i,:,t) .* (ones(num_aux,1)*Massflow_min(t,:)))  : '');
            end
        end
        % aux_M_beta
        if flag_McCormick
            for t = 1:num_heatperiod
                i = 1:num_aux;
                model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                    model_fixed.var.heatingnetwork.aux_M_beta(i,:,t) >= ...
                    aux_beta_min(i,:,t) .* model_fixed.var.heatingnetwork.Massflow(t+num_initialtime+1-i,:) + ...
                    model_fixed.var.heatingnetwork.aux_beta(i,:,t) .* (ones(num_aux,1)*Massflow_min(t,:)) - ...
                    aux_beta_min(i,:,t) .* (ones(num_aux,1)*Massflow_min(t,:)))  : '');
                model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                    model_fixed.var.heatingnetwork.aux_M_beta(i,:,t) >= ...
                    aux_beta_max(i,:,t) .* model_fixed.var.heatingnetwork.Massflow(t+num_initialtime+1-i,:) + ...
                    model_fixed.var.heatingnetwork.aux_beta(i,:,t) .* (ones(num_aux,1)*Massflow_max(t,:)) - ...
                    aux_beta_max(i,:,t) .* (ones(num_aux,1)*Massflow_max(t,:)))  : '');
                model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                    model_fixed.var.heatingnetwork.aux_M_beta(i,:,t) <= ...
                    aux_beta_min(i,:,t) .* model_fixed.var.heatingnetwork.Massflow(t+num_initialtime+1-i,:) + ...
                    model_fixed.var.heatingnetwork.aux_beta(i,:,t) .* (ones(num_aux,1)*Massflow_max(t,:)) - ...
                    aux_beta_min(i,:,t) .* (ones(num_aux,1)*Massflow_max(t,:)))  : '');
                model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                    model_fixed.var.heatingnetwork.aux_M_beta(i,:,t) <= ...
                    aux_beta_max(i,:,t) .* model_fixed.var.heatingnetwork.Massflow(t+num_initialtime+1-i,:) + ...
                    model_fixed.var.heatingnetwork.aux_beta(i,:,t) .* (ones(num_aux,1)*Massflow_min(t,:)) - ...
                    aux_beta_max(i,:,t) .* (ones(num_aux,1)*Massflow_min(t,:)))  : '');
            end
        end
        
        % 0-1 linearization of bilinear terms
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + ((0 <= ...
            model_fixed.var.heatingnetwork.aux_M_alpha) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
            model_fixed.var.heatingnetwork.aux_M_alpha <= ...
            model_fixed.var.heatingnetwork.aux_alpha*big_M) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + ((0 <= ...
            model_fixed.var.heatingnetwork.aux_M_beta) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
            model_fixed.var.heatingnetwork.aux_M_beta <= ...
            model_fixed.var.heatingnetwork.aux_beta*big_M) : '');
    end

