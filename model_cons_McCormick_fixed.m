% % McCormick envelop of aux_alpha_product
    function McCormick_aux_alpha_beta()
        if flag_McCormick
            % % aux_alpha_product
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_alpha_product(1:end-1,:,:) >= ...
                aux_alpha_min(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(2:end,:,:) + ...
                aux_alpha_min(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(1:end-1,:,:) - ...
                aux_alpha_min(1:end-1,:,:) .* aux_alpha_min(2:end,:,:) ...
                ) : '');
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_alpha_product(1:end-1,:,:) >= ...
                aux_alpha_max(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(2:end,:,:) + ...
                aux_alpha_max(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(1:end-1,:,:) - ...
                aux_alpha_max(1:end-1,:,:) .* aux_alpha_max(2:end,:,:) ...
                ) : '');
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_alpha_product(1:end-1,:,:) <= ...
                aux_alpha_min(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(2:end,:,:) + ...
                aux_alpha_max(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(1:end-1,:,:) - ...
                aux_alpha_min(1:end-1,:,:) .* aux_alpha_max(2:end,:,:) ...
                ) : '');
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_alpha_product(1:end-1,:,:) <= ...
                aux_alpha_max(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(2:end,:,:) + ...
                aux_alpha_min(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_alpha(1:end-1,:,:) - ...
                aux_alpha_max(1:end-1,:,:) .* aux_alpha_min(2:end,:,:) ...
                ) : '');
            
            % % aux_beta_product
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_beta_product(1:end-1,:,:) >= ...
                aux_beta_min(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_beta(2:end,:,:) + ...
                aux_beta_min(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_beta(1:end-1,:,:) - ...
                aux_beta_min(1:end-1,:,:) .* aux_beta_min(2:end,:,:) ...
                ) : '');
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_beta_product(1:end-1,:,:) >= ...
                aux_beta_max(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_beta(2:end,:,:) + ...
                aux_beta_max(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_beta(1:end-1,:,:) - ...
                aux_beta_max(1:end-1,:,:) .* aux_beta_max(2:end,:,:) ...
                ) : '');
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_beta_product(1:end-1,:,:) <= ...
                aux_beta_min(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_beta(2:end,:,:) + ...
                aux_beta_max(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_beta(1:end-1,:,:) - ...
                aux_beta_min(1:end-1,:,:) .* aux_beta_max(2:end,:,:) ...
                ) : '');
            model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
                model_fixed.var.heatingnetwork.aux_beta_product(1:end-1,:,:) <= ...
                aux_beta_max(1:end-1,:,:) .* model_fixed.var.heatingnetwork.aux_beta(2:end,:,:) + ...
                aux_beta_min(2:end,:,:) .* model_fixed.var.heatingnetwork.aux_beta(1:end-1,:,:) - ...
                aux_beta_max(1:end-1,:,:) .* aux_beta_min(2:end,:,:) ...
                ) : '');
        end
        
        % % 0-1 linearization of bilinear terms
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + ((0 <= ...
            model_fixed.var.heatingnetwork.aux_alpha_product) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
            model_fixed.var.heatingnetwork.aux_alpha_product(1:end-1,:,:) <= ...
            model_fixed.var.heatingnetwork.aux_alpha(1:end-1,:,:)) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
            model_fixed.var.heatingnetwork.aux_alpha_product(1:end-1,:,:) <= ...
            model_fixed.var.heatingnetwork.aux_alpha(2:end,:,:)) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + ((0 <= ...
            model_fixed.var.heatingnetwork.aux_beta_product) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
            model_fixed.var.heatingnetwork.aux_beta_product(1:end-1,:,:) <= ...
            model_fixed.var.heatingnetwork.aux_beta(1:end-1,:,:)) : '');
        model_fixed.cons_McCormick = model_fixed.cons_McCormick + (( ...
            model_fixed.var.heatingnetwork.aux_beta_product(1:end-1,:,:) <= ...
            model_fixed.var.heatingnetwork.aux_beta(2:end,:,:)) : '');
    end

