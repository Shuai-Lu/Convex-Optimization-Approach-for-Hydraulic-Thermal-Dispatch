            model_fixed.var.heatingnetwork.aux_M_beta_1 = sdpvar(num_aux,num_pipe,num_heatperiod);
            model_fixed.var.heatingnetwork.aux_M_beta_2 = sdpvar(num_aux,num_pipe,num_heatperiod);
            model_fixed.var.heatingnetwork.aux_h_pipe_s_in_alpha_1 = sdpvar(num_aux,num_pipe,num_heatperiod);
            model_fixed.var.heatingnetwork.aux_h_pipe_s_in_alpha_2 = sdpvar(num_aux,num_pipe,num_heatperiod);
