%% -----------------------------------------------------------------
% % Define variables
    function define_variables()
        model_fixed.var.heatingnetwork = [];
        model_fixed.cost.heatingnetwork.pump = sdpvar(num_initialtime+num_heatperiod,1);
        model_fixed.cost.heatingnetwork.sum = sdpvar(1,1);
        model_fixed.var.heatingnetwork.Massflow = sdpvar(num_initialtime+num_heatperiod, num_pipe);   % t/h
        model_fixed.var.heatingnetwork.Pressure_loss = sdpvar(num_initialtime+num_heatperiod, num_pipe);
        model_fixed.var.heatingnetwork.Pressure_ecv = sdpvar(num_initialtime+num_heatperiod, num_pipe);
        model_fixed.var.heatingnetwork.Pressure_node = sdpvar(num_initialtime+num_heatperiod, num_node);
        model_fixed.var.heatingnetwork.Pressure_pump = sdpvar(num_initialtime+num_heatperiod, 1);
