% % Cons of hydraulic
    function Cons_hydraulic()
        % % mass flow balance (4i)
        t = num_start:num_end;
        for k = 1:num_node
            % cross node
            if data_node(k,loc_nodetype) == 1
                set_pipe_head = find(data_pipe(:,loc_fnode) == k);
                set_pipe_tail = find(data_pipe(:,loc_tnode) == k);
                if isa((sum(model_fixed.var.heatingnetwork.Massflow(t,set_pipe_tail),2) == ...
                        sum(model_fixed.var.heatingnetwork.Massflow(t,set_pipe_head),2)), 'constraint')
                    model_fixed.cons = model_fixed.cons + (( ...
                        sum(model_fixed.var.heatingnetwork.Massflow(t,set_pipe_tail),2) == ...
                        sum(model_fixed.var.heatingnetwork.Massflow(t,set_pipe_head),2)) : '');
                end
            end
        end
        % % pressure head balance of pipeline (4j)
        t = num_start:num_end;
        for j = 1:num_pipe
            fnode = data_pipe(j,loc_fnode);
            tnode = data_pipe(j,loc_tnode);
            model_fixed.cons = model_fixed.cons + (( ...
                model_fixed.var.heatingnetwork.Pressure_node(t,fnode) - ...
                model_fixed.var.heatingnetwork.Pressure_node(t,tnode) == ...
                model_fixed.var.heatingnetwork.Pressure_loss(t,j) + ...
                model_fixed.var.heatingnetwork.Pressure_ecv(t,j)) : '');
        end
        model_fixed.cons = model_fixed.cons + (( ...
            model_fixed.var.heatingnetwork.Pressure_node(t,1) == ...
            model_fixed.var.heatingnetwork.Pressure_pump(t,1)) : '');
        
        % % pressure loss of pipeline (4k): (10b)
        t = 1:num_heatperiod;
        j = 1:num_pipe;
        model_fixed.cons = model_fixed.cons + (( ...
            model_fixed.var.heatingnetwork.Pressure_loss(t+num_initialtime,j) >= ...
            ones(num_heatperiod,1)*data_pipe(j,loc_rough)'.* ...
            model_fixed.var.heatingnetwork.Massflow(t+num_initialtime,j).^2/3.6^2) : '');
        model_fixed.cons = model_fixed.cons + (( ...
            model_fixed.var.heatingnetwork.Pressure_loss(t+num_initialtime,j) <= ...
            ones(num_heatperiod,1)*data_pipe(j,loc_rough)'.* ...
            ((Massflow_max(t,j)+Massflow_min(t,j)).* ...
            model_fixed.var.heatingnetwork.Massflow(t+num_initialtime,j) - ...
            Massflow_max(t,j).*Massflow_min(t,j))/3.6^2) : '');
        
        % % pressure of electrical control valve (ecv)
        t = num_start:num_end;
        for j = 1:num_pipe
            if ~data_pipe(j,loc_flag_ecv)
                model_fixed.cons = model_fixed.cons + (( ...
                    model_fixed.var.heatingnetwork.Pressure_ecv(t,j) == 0) : '');
            else
                model_fixed.cons = model_fixed.cons + (( ...
                    model_fixed.var.heatingnetwork.Pressure_ecv(t,j) >= 0) : '');
            end
        end
        
        % % bounds of mass flow and pressure head (4m)-4(n)
        t = 1:num_heatperiod;
        if isa((Massflow_min(t,:) <= ...
                model_fixed.var.heatingnetwork.Massflow(t+num_initialtime,:) <= ...
                Massflow_max(t,:)), 'constraint')
            model_fixed.cons = model_fixed.cons + ((Massflow_min(t,:) ...
                <= model_fixed.var.heatingnetwork.Massflow(t+num_initialtime,:) <= ...
                Massflow_max(t,:)) : '');
        end
        model_fixed.cons = model_fixed.cons + (( ...
            Pressure_node_min <= ...
            model_fixed.var.heatingnetwork.Pressure_node(t+num_initialtime,:) <= ...
            Pressure_node_max) : '');
    end

