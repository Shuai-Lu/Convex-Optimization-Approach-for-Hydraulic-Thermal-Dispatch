%% -----------------------------------------------------------------
% % Initialize num_initialtime
    function Initilize_initialtime()
        model_fixed.var.heatingnetwork.Tau_pipe_s_in(1:num_initialtime,:) = data_initial.temperature(loc_initial_Tau_s);
        model_fixed.var.heatingnetwork.Tau_pipe_r_in(1:num_initialtime,:) = data_initial.temperature(loc_initial_Tau_r);
        model_fixed.var.heatingnetwork.Tau_pipe_s_out(1:num_initialtime,:) = data_initial.temperature(loc_initial_Tau_s);
        model_fixed.var.heatingnetwork.Tau_pipe_r_out(1:num_initialtime,:) = data_initial.temperature(loc_initial_Tau_r);
        model_fixed.var.heatingnetwork.Massflow(1:num_initialtime,:) = ones(num_initialtime,1)*data_initial.massflow;
        model_fixed.var.heatingnetwork.h_pipe_s_in(1:num_initialtime,:) = heat_coefficient* ...
            model_fixed.var.heatingnetwork.Massflow(1:num_initialtime,:).*model_fixed.var.heatingnetwork.Tau_pipe_s_in(1:num_initialtime,:);
        model_fixed.var.heatingnetwork.h_pipe_s_out(1:num_initialtime,:) = heat_coefficient* ...
            model_fixed.var.heatingnetwork.Massflow(1:num_initialtime,:).*model_fixed.var.heatingnetwork.Tau_pipe_s_out(1:num_initialtime,:);
        model_fixed.var.heatingnetwork.h_pipe_r_in(1:num_initialtime,:) = heat_coefficient* ...
            model_fixed.var.heatingnetwork.Massflow(1:num_initialtime,:).*model_fixed.var.heatingnetwork.Tau_pipe_r_in(1:num_initialtime,:);
        model_fixed.var.heatingnetwork.h_pipe_r_out(1:num_initialtime,:) = heat_coefficient* ...
            model_fixed.var.heatingnetwork.Massflow(1:num_initialtime,:).*model_fixed.var.heatingnetwork.Tau_pipe_r_out(1:num_initialtime,:);
    end

%% -----------------------------------------------------------------
% % fixed known variables
    function Fix_known_variables()
        for t = 1:num_heatperiod
            for j = 1:num_pipe
                % Massflow
                if Massflow_max(t,j) == Massflow_min(t,j)
                    model_fixed.var.heatingnetwork.Massflow(t+num_initialtime,j) = ...
                        Massflow_min(t,j);
                end
                % Tau_pipe
                if Tau_pipe_s_in_max(t,j) == Tau_pipe_s_in_min(t,j)
                    model_fixed.var.heatingnetwork.Tau_pipe_s_in(t+num_initialtime,j) = ...
                        Tau_pipe_s_in_min(t,j);
                end
                if Tau_pipe_s_out_max(t,j) == Tau_pipe_s_out_min(t,j)
                    model_fixed.var.heatingnetwork.Tau_pipe_s_out(t+num_initialtime,j) = ...
                        Tau_pipe_s_out_min(t,j);
                end
                if Tau_pipe_r_in_max(t,j) == Tau_pipe_r_in_min(t,j)
                    model_fixed.var.heatingnetwork.Tau_pipe_r_in(t+num_initialtime,j) = ...
                        Tau_pipe_r_in_min(t,j);
                end
                if Tau_pipe_r_out_max(t,j) == Tau_pipe_r_out_min(t,j)
                    model_fixed.var.heatingnetwork.Tau_pipe_r_out(t+num_initialtime,j) = ...
                        Tau_pipe_r_out_min(t,j);
                end
            end
        end
        % % fixed known aux variables
        temp = (aux_alpha_max == aux_alpha_min);
        model_fixed.cons = model_fixed.cons + (( ...
            model_fixed.var.heatingnetwork.aux_alpha(temp) == ...
            aux_alpha_min(temp)) : '');
        temp = (aux_beta_max == aux_beta_min);
        model_fixed.cons = model_fixed.cons + (( ...
            model_fixed.var.heatingnetwork.aux_beta(temp) == ...
            aux_beta_min(temp)) : '');
    end

%% -----------------------------------------------------------------
% % Thermal balance at source & load nodes
    function Cons_ThermalBalance_source_load()
        t = num_start:num_end;
        for k = 1:num_node
            % heat power balance at source node (4a)
            if data_node(k,loc_nodetype) == 0
                j = find(data_pipe(:,loc_fnode) == k);
                model_fixed.cons = model_fixed.cons + ((model_fixed.var.heatingnetwork.h_source(t,:) == ...
                    model_fixed.var.heatingnetwork.h_pipe_s_in(t,j) - ...
                    model_fixed.var.heatingnetwork.h_pipe_r_out(t,j)) : 'h_source'); %#ok<*BDSCA>
                % heat power balance at crossing node
            elseif data_node(k,loc_nodetype) == 1
                % heat power balance at load node (4b)
            elseif data_node(k,loc_nodetype) == 2
                j = find(data_pipe(:,loc_tnode) == k);
                model_fixed.cons = model_fixed.cons + (( ...
                    model_fixed.var.heatingnetwork.h_load(t, ...
                    sum(data_node(1:k,loc_nodetype) == 2)) == ...
                    model_fixed.var.heatingnetwork.h_pipe_s_out(t,j) - ...
                    model_fixed.var.heatingnetwork.h_pipe_r_in(t,j)) : 'h_load');
            end
        end
    end

%% -----------------------------------------------------------------
% % Thermal balance at intersection node
    function Cons_ThermalBalance_node()
        for k = 1:num_node
            % cross node
            if data_node(k,loc_nodetype) == 1
                set_pipe_head = find(data_pipe(:,loc_fnode) == k);
                set_pipe_tail = find(data_pipe(:,loc_tnode) == k);
                model_fixed.cons = model_fixed.cons + (( ...
                    sum(model_fixed.var.heatingnetwork.h_pipe_s_out(num_start:num_end,set_pipe_tail),2) == ...
                    sum(model_fixed.var.heatingnetwork.h_pipe_s_in(num_start:num_end,set_pipe_head),2)) : '');
                model_fixed.cons = model_fixed.cons + (( ...
                    sum(model_fixed.var.heatingnetwork.h_pipe_r_out(num_start:num_end,set_pipe_head),2) == ...
                    sum(model_fixed.var.heatingnetwork.h_pipe_r_in(num_start:num_end,set_pipe_tail),2)) : '');
                if length(set_pipe_head) >= 2
                    for j = 2:length(set_pipe_head)
                        model_fixed.cons = model_fixed.cons + (( ...
                            model_fixed.var.heatingnetwork.Tau_pipe_s_in(num_start:num_end,set_pipe_head(1)) == ...
                            model_fixed.var.heatingnetwork.Tau_pipe_s_in(num_start:num_end,set_pipe_head(j))) : '');
                    end
                end
                if length(set_pipe_tail) >= 2
                    for j = 2:length(set_pipe_tail)
                        model_fixed.cons = model_fixed.cons + (( ...
                            model_fixed.var.heatingnetwork.Tau_pipe_r_in(:,set_pipe_tail(1)) == ...
                            model_fixed.var.heatingnetwork.Tau_pipe_r_in(:,set_pipe_tail(j))) : '');
                    end
                end
            end
        end
    end

%% -----------------------------------------------------------------
% % Complementary constraints between aux_alpha aux_beta
    function Cons_aux_alpha_beta()
        % 0 <= aux_alpha <= 1
        if isa((0 <= model_fixed.var.heatingnetwork.aux_alpha(:,:,:) <= 1), 'constraint')
            model_fixed.cons = model_fixed.cons + ((0 <= ...
                model_fixed.var.heatingnetwork.aux_alpha(:,:,:) <= 1) : '');
        end
        % aux_alpha(i+1) - aux_alpha_product(i) = 0
