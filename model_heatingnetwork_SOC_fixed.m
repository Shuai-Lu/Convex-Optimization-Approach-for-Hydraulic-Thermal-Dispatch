        model_fixed.var.heatingnetwork.aux_beta = sdpvar(num_aux,num_pipe,num_heatperiod);
        model_fixed.var.heatingnetwork.aux_M_alpha = sdpvar(num_aux,num_pipe,num_heatperiod);
        model_fixed.var.heatingnetwork.aux_M_beta = sdpvar(num_aux,num_pipe,num_heatperiod);
        model_fixed.var.heatingnetwork.aux_h_pipe_s_in_alpha = sdpvar(num_aux,num_pipe,num_heatperiod);
        model_fixed.var.heatingnetwork.aux_h_pipe_s_in_beta = sdpvar(num_aux,num_pipe,num_heatperiod);
        model_fixed.var.heatingnetwork.aux_h_pipe_r_in_alpha = sdpvar(num_aux,num_pipe,num_heatperiod);
        model_fixed.var.heatingnetwork.aux_h_pipe_r_in_beta = sdpvar(num_aux,num_pipe,num_heatperiod);
        model_fixed.var.heatingnetwork.aux_h_pipe_s_out = sdpvar(num_heatperiod, num_pipe);
        model_fixed.var.heatingnetwork.aux_h_pipe_r_out = sdpvar(num_heatperiod, num_pipe);
        
        % % soc
        if flag_SOC
            model_fixed.var.heatingnetwork.aux_alpha_product_1 = sdpvar(num_aux,num_pipe,num_heatperiod);
